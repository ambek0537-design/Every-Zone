// Localized helper to get translation or fallback
export function getLocalizedData(item: any, fieldName: string, lang: string): string {
  if (!item) return '';
  const itemLangData = LISTINGS_TRANSLATIONS[lang]?.[item.id];
  if (itemLangData && itemLangData[fieldName]) {
    return itemLangData[fieldName];
  }
  // Fallback map: if am, ti, om, ar is selected and we don't have list translations, use am (or en)
  if (lang === 'en') {
    return item[fieldName] || '';
  }
  const amField = fieldName + 'Am';
  const enField = fieldName;
  return item[amField] || item[enField] || '';
}

// Full helper function for listing features/requirements
export function getLocalizedArray(item: any, fieldName: 'features' | 'requirements', lang: string): string[] {
  if (!item) return [];
  const itemLangData = LISTINGS_TRANSLATIONS[lang]?.[item.id];
  if (itemLangData && itemLangData[fieldName]) {
    return itemLangData[fieldName];
  }
  
  if (lang === 'en') {
    return item[fieldName] || [];
  }
  const amField = fieldName + 'Am';
  return item[amField] || item[fieldName] || [];
}
